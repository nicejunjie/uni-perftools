#!/usr/bin/env python3
"""Generate the descriptor table, slot enum, and wrapper bodies for upat.

One wrapper body is emitted per symbol; the symbol *name* is chosen at compile
time by the WRAP() macro (real name for preload, libprof_dbi_* for frida), so the
same generated .c serves both backends. Output goes to <outdir>.

Prototype line format (Fortran dialect): one C signature per line, e.g.
    void dgemm_(char* transa, ..., libprof_int_t* ldc)
Lines starting with '#' or blank are ignored.
"""
import re
import os
import sys
import argparse

# (prototype file, display group, group enum, dialect) in slot-assignment order.
# dialect "fortran": all args are pointers; "c": args are by value.
GROUPS = [
    ("blas.txt",      "BLAS",      "LP_BLAS",      "fortran"),
    ("lapack.txt",    "LAPACK",    "LP_LAPACK",    "fortran"),
    ("pblas.txt",     "PBLAS",     "LP_PBLAS",     "fortran"),
    ("scalapack.txt", "ScaLAPACK", "LP_SCALAPACK", "fortran"),
    ("cblas.txt",     "CBLAS",     "LP_CBLAS",     "c"),
    ("lapacke.txt",   "LAPACKe",   "LP_LAPACKE",   "c"),
    ("fftw.txt",      "FFTW",      "LP_FFTW",      "c"),
    # MPI uses the "opaque" dialect: wrappers take uniform void* args and never
    # include <mpi.h>. MPI C calls pass only int/pointer/handle args, each one
    # 8-byte slot on x86-64 SysV and aarch64 AAPCS, so forwarding void* verbatim
    # is ABI-correct whether a handle is a 4-byte int (MPICH) or 8-byte pointer
    # (OpenMPI). This sidesteps the constant/handle portability bugs (the ones
    # that break mpiP on OpenMPI) and removes the mpicc build dependency.
    ("mpi.txt",       "MPI",       "LP_MPI",       "opaque"),
    ("io.txt",        "IO",        "LP_IO",        "opaque"),
]

# extra header a group's wrapper file must include (for its argument types)
GROUP_INCLUDE = {}

# Return types that are pointers (so the analyzer receives the return value).
POINTER_RET = {"fftw_plan", "fftwf_plan", "fftwl_plan"}

def is_pointer_ret(ret):
    return "*" in ret or ret in POINTER_RET

SIG_RE = re.compile(r'^\s*(.+?)\s+(\w+)\s*\((.*)\)\s*$')


def parse_proto(line):
    """Return (ret_type, func_name, args_text, arg_names) or None."""
    m = SIG_RE.match(line)
    if not m:
        return None
    ret, name, args = m.group(1).strip(), m.group(2), m.group(3).strip()
    if args == "" or args.lower() == "void":
        arg_names = []
        args = "void"
    else:
        arg_names = [a.split()[-1].strip("*").strip() for a in args.split(",")]
    return ret, name, args, arg_names


def collect(protodir):
    """Return ordered list of records, deduped by symbol (first wins)."""
    seen = {}
    records = []
    for fname, group, gid, dialect in GROUPS:
        path = os.path.join(protodir, fname)
        if not os.path.exists(path):
            continue
        with open(path) as f:
            for line in f:
                line = re.sub(r'#.*', '', line).strip().rstrip(';').strip()
                if not line:
                    continue
                p = parse_proto(line)
                if not p:
                    sys.stderr.write("WARN: cannot parse: %s\n" % line)
                    continue
                ret, name, args, arg_names = p
                if name in seen:
                    continue
                seen[name] = True
                records.append((ret, name, args, arg_names, group, gid, dialect))
    return records


def emit_slots(records, out):
    with open(os.path.join(out, "libprof_slots.h"), "w") as f:
        f.write("/* generated by gen/gen.py - do not edit */\n")
        f.write("#ifndef LIBPROF_SLOTS_H\n#define LIBPROF_SLOTS_H\n")
        f.write("enum libprof_slot_id {\n")
        for (_, name, *_rest) in records:
            f.write("    SLOT_%s,\n" % name)
        f.write("    LIBPROF_NSLOTS\n};\n")
        f.write("#endif\n")


def emit_desc(records, out):
    with open(os.path.join(out, "libprof_desc.c"), "w") as f:
        f.write("/* generated by gen/gen.py - do not edit */\n")
        f.write('#include "libprof.h"\n\n')
        f.write("libprof_desc_t libprof_desc[LIBPROF_NSLOTS] = {\n")
        for (ret, name, args, arg_names, group, gid, dialect) in records:
            f.write('  [SLOT_%s] = { "%s", "libprof_dbi_%s", "%s", %s, SLOT_%s, NULL, NULL },\n'
                    % (name, name, name, group, gid, name))
        f.write("};\n")


def emit_wrapper(f, ret, name, args, arg_names, dialect):
    is_void = (ret == "void")
    # opaque (MPI): ignore declared types, take/forward uniform void* slots
    if dialect == "opaque":
        arg_names = ["a%d" % i for i in range(len(arg_names))]
        args = ", ".join("void* %s" % a for a in arg_names) if arg_names else "void"
    call_args = ", ".join(arg_names)
    fp = "%s (*)()" % ret
    addr = "&" if dialect in ("c", "opaque") else ""  # by-value args passed by address
    # opaque (MPI/IO) returns an int/ssize_t we want (e.g. bytes actually read);
    # otherwise only capture pointer returns (e.g. fftw_plan).
    if dialect == "opaque" and not is_void:
        retexpr = "(void*)(intptr_t)lp__r"
    else:
        retexpr = "(void*)lp__r" if is_pointer_ret(ret) else "(void*)0"
    if arg_names:
        argarr = "        void *lp__args[] = { %s };\n" % ", ".join("(void*)%s%s" % (addr, a) for a in arg_names)
    else:
        argarr = "        void **lp__args = (void **)0;\n"

    f.write("%s WRAP(%s)(%s)\n{\n" % (ret, name, args))
    f.write("    libprof_desc_t *lp__d = &libprof_desc[SLOT_%s];\n" % name)
    f.write("    if (__builtin_expect(lp__d->orig == NULL, 0)) libprof_resolve(lp__d);\n")
    f.write("    libprof_frame_t *lp__fr = libprof_enter();\n")
    if is_void:
        f.write("    if (__builtin_expect(lp__fr == NULL, 0)) { ((%s)lp__d->orig)(%s); return; }\n" % (fp, call_args))
        f.write("    ((%s)lp__d->orig)(%s);\n" % (fp, call_args))
    else:
        f.write("    if (__builtin_expect(lp__fr == NULL, 0)) return ((%s)lp__d->orig)(%s);\n" % (fp, call_args))
        f.write("    %s lp__r = ((%s)lp__d->orig)(%s);\n" % (ret, fp, call_args))
    f.write("    double lp__dt = libprof_now() - lp__fr->t0;\n")
    # analyzer path: args array built only when an analyzer is registered (cold for most)
    f.write("    if (__builtin_expect(lp__d->analyze != 0, 0)) {\n")
    f.write(argarr)
    f.write("        libprof_key_t lp__k; libprof_delta_t lp__md = {0};\n")
    f.write("        int lp__sh = lp__d->analyze(&lp__k, &lp__md, lp__d, lp__args, %s);\n" % retexpr)
    f.write("        libprof_exit(lp__d, lp__dt, lp__sh ? &lp__k : (libprof_key_t*)0, &lp__md);\n")
    f.write("    } else {\n")
    f.write("        libprof_exit(lp__d, lp__dt, (libprof_key_t*)0, (libprof_delta_t*)0);\n")
    f.write("    }\n")
    f.write("    return%s;\n" % (" lp__r" if not is_void else ""))
    f.write("}\n\n")


def emit_wrappers(records, out):
    # one .c per display group keeps compile units reasonable
    by_group = {}
    for r in records:
        by_group.setdefault(r[4], []).append(r)
    files = []
    for group, recs in by_group.items():
        fn = os.path.join(out, "%s_wrap.c" % group.lower())
        files.append(fn)
        with open(fn, "w") as f:
            f.write("/* generated by gen/gen.py - do not edit */\n")
            if group in GROUP_INCLUDE:
                f.write(GROUP_INCLUDE[group] + "\n")
            f.write('#include "libprof_wrap.h"\n\n')
            for (ret, name, args, arg_names, g, gid, dialect) in recs:
                emit_wrapper(f, ret, name, args, arg_names, dialect)
    return files


def main():
    ap = argparse.ArgumentParser()
    ap.add_argument("--protodir", default=os.path.join(os.path.dirname(__file__), "prototypes"))
    ap.add_argument("--out", required=True)
    args = ap.parse_args()
    os.makedirs(args.out, exist_ok=True)
    records = collect(args.protodir)
    emit_slots(records, args.out)
    emit_desc(records, args.out)
    emit_wrappers(records, args.out)
    sys.stderr.write("gen.py: %d functions across %d groups\n"
                     % (len(records), len({r[4] for r in records})))


if __name__ == "__main__":
    main()
